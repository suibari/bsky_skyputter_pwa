	<section class="mb-6">
		<h2 class="text-base font-semibold text-gray-800 dark:text-gray-200 mb-2">1. はじめに</h2>
		<p class="text-sm text-gray-600 dark:text-gray-400 leading-relaxed">
			SkyPutter（以下「本アプリ」）は、Bluesky (AT Protocol) を利用した半投稿専用 PWA クライアントです。
			本ポリシーでは、本アプリが収集・利用する情報について説明します。
		</p>
	</section>

	<section class="mb-6">
		<h2 class="text-base font-semibold text-gray-800 dark:text-gray-200 mb-2">2. 収集する情報</h2>
		<ul class="text-sm text-gray-600 dark:text-gray-400 leading-relaxed space-y-2 list-disc list-inside">
			<li>
				<strong>Bluesky 認証情報:</strong> ログイン時に取得する DID・ハンドル・アクセストークン。
				端末のローカルストレージに保存され、API リクエストの認証に使用されます。
			</li>
			<li>
				<strong>Push 通知トークン:</strong> Web Push 通知を有効にした場合、ブラウザが発行するサブスクリプション情報をサーバーに保存します。
				通知の配信にのみ使用し、それ以外の目的には使用しません。
			</li>
			<li>
				<strong>下書きデータ:</strong> 作成した下書きは、ユーザー自身の Bluesky PDS（Personal Data Server）に保存されます。
				本アプリのサーバーおよびブラウザには保存されません。
			</li>
		</ul>
	</section>

	<section class="mb-6">
		<h2 class="text-base font-semibold text-gray-800 dark:text-gray-200 mb-2">3. 情報の利用目的</h2>
		<ul class="text-sm text-gray-600 dark:text-gray-400 leading-relaxed space-y-2 list-disc list-inside">
			<li>Bluesky への投稿・通知取得などのアプリ機能の提供</li>
			<li>Web Push 通知の配信</li>
		</ul>
	</section>

	<section class="mb-6">
		<h2 class="text-base font-semibold text-gray-800 dark:text-gray-200 mb-2">4. 第三者への提供</h2>
		<p class="text-sm text-gray-600 dark:text-gray-400 leading-relaxed">
			本アプリは、Bluesky (AT Protocol) の公開 API を通じてのみデータをやり取りします。
			収集した個人情報を、法令に基づく場合を除き第三者に提供・開示することはありません。
		</p>
	</section>

	<section class="mb-6">
		<h2 class="text-base font-semibold text-gray-800 dark:text-gray-200 mb-2">5. データの削除</h2>
		<p class="text-sm text-gray-600 dark:text-gray-400 leading-relaxed">
			Push 通知の登録解除はアプリの設定画面から行えます。
			ブラウザのデータ消去またはアプリのアンインストールにより、端末上のデータ（認証情報・下書き）はすべて削除されます。
		</p>
	</section>

